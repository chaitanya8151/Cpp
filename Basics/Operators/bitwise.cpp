#include<iostream>
using namespace std;
int main(){
    //bitwise and
    cout<<"bitwise and operator: "<<endl;
    cout<<(5&7)<<endl;
    cout<<(5&2)<<endl;

    //bitwise or operator
    cout<<"Bitwise or operator:"<<endl;
    cout<<(10|5)<<endl;

    //bitwise xor operator
    cout<<"Bitwise xor operator:"<<endl;
    cout<<(5^7)<<endl;
    cout<<(5^5);
    return 0;   
}